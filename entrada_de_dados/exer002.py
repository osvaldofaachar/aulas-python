num1 = float(input("Digite o primeiro numero: "))
num2 = float(input("Digite o segundo numero: "))

soma = num1 + num2
sub = num1 - num2
mul = num1 * num2
div = num1 / num2

print(f"A Soma entre {num1} e {num2} é {soma}")
print(f"A Diferença entre {num1} e {num2} é {sub}")
print(f"O Produto da Multiplicação entre {num1} e {num2} é {mul}")
print(f"O Quociente da Divisão entre {num1} e {num2} é {div}")